// Helper function to get products by badge
export const getProductsByBadge = (badge) => {
  return allProducts.filter(product => product.badge === badge);
};

// Helper function to get products by category
export const getProductsByCategory = (category) => {
  if (category === 'All') return allProducts;
  return allProducts.filter(product => product.category === category);
};

// Get bestseller products
export const getBestsellers = () => {
  return allProducts.filter(product => product.badge === 'Bestseller' || product.rating >= 4.5).slice(0, 8);
};

// Get new products
export const getNewProducts = () => {
  return allProducts.filter(product => product.badge === 'New' || product.id <= 8).slice(0, 8);
};

// Get product by ID
export const getProductById = (id) => {
  return allProducts.find(product => product.id === parseInt(id));
};

// Get all products grouped by category (for CategoryProducts page)
export const getProductsByCategories = () => {
  const grouped = {};
  
  allProducts.forEach(product => {
    const categoryKey = product.category.toLowerCase().replace(/\s+/g, '-');
    if (!grouped[categoryKey]) {
      grouped[categoryKey] = [];
    }
    grouped[categoryKey].push(product);
  });
  
  return grouped;
};
